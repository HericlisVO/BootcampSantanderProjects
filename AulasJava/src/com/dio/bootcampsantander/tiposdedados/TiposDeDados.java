package com.dio.bootcampsantander.tiposdedados;

public class TiposDeDados {
	
public static void main(String[] args) {
	// Estatico vs Dinamico
	
	//Primitivo vs Composto
	
	//tipos textual, numeral. lógico e objeto

	//nemerais = byte, short, int, long, float, double
	
	byte b1 = 20;
	
	byte b2 = 30;
	
	short s1 = 20000;
	
	//ultrapassando quantidade permitida
	//short s2 = 40000;
	
	//valor não permitido
	int i1 = -1111111111;
	
	int i2 = 2000000000;
	
	//terminar com L para indicar que é Long
	long l2 = 1000034435242342343L;
	long l1 = 2254354544342433434L;
	
	//indicar que é float com f ao final
	float f1 = 4.5f;
	
	
	// é opcional colocar d no final para declarar que é double
	double d1 = 54.54;
	
	double d2 = 334.3d;
	//float f1 = 4.5f;
	
	// textual char caracteres de 16 bit
	
	//só permite um caracter
	char c1 = 'R';
	//char c2 = ' Te';
	char c3 = '\u0057';
	
	// String, Objeto textual para inserir cadeia de caracter
	
	String st1 = "Hericlis Ventura";
	
	String st2 = "Outro";
	
	String st3 = "098/03/98";
	//lógico = boolean
	boolean bn1 = true;
	boolean bn2= false;
	
	
	//objeto = referencia de uma classe
	
	
	System.out.println(b1);
	System.out.println(b2);
	
	
	System.out.println(s1);
	
	
	System.out.println(i1);
	System.out.println(i2);
	
	
	System.out.println(l1);
	System.out.println(l2);
	
	
	System.out.println(f1);
	
	
	System.out.println(d1);
	System.out.println(d2);
	
	
	System.out.println(c1);
	System.out.println(c3);
	
	
	System.out.println(st1);
	System.out.println(st2);
	System.out.println(st3);
	
	System.out.println(b1);
	System.out.println(b2);
	
}
	
	
}
